list1 = ['Maruti', 'Tata', 'Honda', 'Toyota', 'Mahindra']
print(list1)
list2 = [21, 87, 54, 65, 22, 86]
print(list2)

"""Sort the list in the ascending order"""
print("Sort the list")
list1.sort()
print(list1)
list2.sort()
print(list2)

"""Reverse the list"""
print("Reversed the ordered of the list")
list1.reverse()
print(list1)
list2.reverse()
print(list2)
